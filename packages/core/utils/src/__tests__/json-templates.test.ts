import { parse } from '../json-templates';

describe('json-templates', () => {
  it('parse json with string template', async () => {
    const template = {
      name: '{{id}}-{{name}}.',
      age: 18,
    };
    const result = parse(template)({
      name: 'test',
      id: 1,
    });
    expect(result).toEqual({
      name: '1-test.',
      age: 18,
    });
  });

  it('when key is class', async () => {
    const date = new Date();
    const template = {
      key1: '{{date}}',
    };
    const result = parse(template)({
      date,
    });
    expect(result).toEqual({
      key1: date,
    });
  });
});
